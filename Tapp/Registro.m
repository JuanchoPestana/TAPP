#import "Registro.h"

@implementation Registro

// SETTERS
-(void) setCorreo:(NSString *) the_correo{
    
    correo_objeto = the_correo;
}

-(void) setNip:(NSString *) the_nip{
    
    nip_objeto = the_nip;
}

///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
// GETTERS

- (NSString *)getCorreo{
    return correo_objeto;
}

- (NSString *)getNip{
    return nip_objeto;
}

///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////
// REGISTRO GLOBAL

+ (id)registroGlobal {
    
    static Registro *global = nil;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        global = [[self alloc] init];
    });
    return global;
}


@end
